class HomeSearch
{
  String searchContent;
  String date;

  setSearchContent(String searchContent)
  {
    this.searchContent = searchContent;
  }

  getSearchContent()
  {
    return this.searchContent;
  }

  setDate(String date)
  {
    this.date = date;
  }

  getDate()
  {
    return this.date;
  }
}